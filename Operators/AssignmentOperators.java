/**
 * AssignmentOperators
 */
public class AssignmentOperators {

    public static void main(String[] args) {
        double a = 10; //  = as assignment operator
        a += 5; // a = a +5 = 15
        System.out.println(a); // 15
        System.out.println(a *= 2); // a = a * 2 = 30
        System.out.println(a /= 6); // a = a / 6 = 5
        System.out.println(a %= 7); // a = a % 6 = 5
        System.out.println(5%6); // a = a % 6 = 5
        int b = 56;
        b -= 9; // b = b - 9
        System.out.println(b);
    }
}