public class ComparisonOperators {
    public static void main(String[] args) {
        char c1 = 'A';
        char c2  = 'a';
        String s1 = "Rao";
        String s2 = "Rao";
        float f1 = 4.44f;
        float f2 = 2348.6f;
        System.out.println(c1==c2);
        System.out.println(s1!=s2);
        System.out.println(f1>f2);
        System.out.println(f1>=f2);
        System.out.println(f1<f2);
        System.out.println(f1<=f2);
    }
}
