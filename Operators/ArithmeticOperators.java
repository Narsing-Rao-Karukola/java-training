public class ArithmeticOperators{
    public static void main(String[] args){
        int a = 10;
        int b = 2;
        System.out.println("Hi" + " Hello" + 10);
        System.out.println(" + "+a+b);
        System.out.println(" - "+(a-b));
        System.out.println(" * "+(a*b));
        System.out.println(" / "+(a/b)); // divdent 
        System.out.println(" % "+(a%b)); // remainder 
        System.out.println(1 + 2.9); // remainder 
        System.out.println('a'+"erfh"); // remainder 
    }
}